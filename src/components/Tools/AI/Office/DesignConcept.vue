<script setup lang="ts">
import { ref, reactive } from 'vue'
import { useRoute } from 'vue-router'
import { ElMessage } from 'element-plus'
import ToolsRecommend from '@/components/Common/ToolsRecommend.vue'
import { generateAIWriting } from '@/services/ai'

const route = useRoute()
const mode = ref<'editable' | 'preview' | 'edit'>('editable')
const form = reactive({
  project: '',
  style: '',
  elements: '',
  inspiration: ''
})

const isGenerating = ref(false)
const resultText = ref('')

const generateContent = async () => {
  if (!form.project) {
    ElMessage.warning('请输入项目名称')
    return
  }

  try {
    isGenerating.value = true
    resultText.value = ''

    const prompt = `请帮我撰写一份设计理念阐述。
项目名称：${form.project}
${form.style ? `设计风格：${form.style}` : ''}
${form.elements ? `核心元素：${form.elements}` : ''}
${form.inspiration ? `灵感来源：${form.inspiration}` : ''}

要求：
1. 语言优美、专业，富有艺术感。
2. 阐述设计背后的思考、逻辑和价值。
3. 结合风格和元素，描绘画面感。
4. 标题请使用 Markdown 三级标题格式（### 标题），严禁在标题行使用 ** 加粗符号。`

    await generateAIWriting({
      prompt,
      systemPrompt: '你是一个资深的设计总监，擅长用文字表达设计美学和理念。',
      temperature: 0.8
    }, (content) => {
      resultText.value += content
    })

    ElMessage.success('生成完成')
  } catch (error) {
    ElMessage.error('生成失败，请稍后重试')
  } finally {
    isGenerating.value = false
  }
}

const handleAiAssist = async (type: string) => {
  if (!resultText.value) return

  let prompt = ''
  let systemPrompt = ''

  switch (type) {
    case 'polish':
      prompt = `请润色上述设计理念，使语言更加优美、富有艺术感和感染力，增强画面描述。

      当前设计理念：
      ${resultText.value}`
      systemPrompt = '你是一个文采斐然的设计评论家。'
      break
    case 'expand':
      prompt = `请基于上述设计理念，拓展更多的应用场景描述和细节延展，说明设计在不同媒介下的表现。

      当前设计理念：
      ${resultText.value}`
      systemPrompt = '你是一个注重落地的设计管理者。'
      break
    case 'elevate':
      prompt = `请升华上述设计理念，将其与品牌价值、社会意义或文化内涵相结合，提升设计的高度和深度。

      当前设计理念：
      ${resultText.value}`
      systemPrompt = '你是一个具有战略眼光的品牌顾问。'
      break
  }

  try {
    isGenerating.value = true
    const currentContent = resultText.value
    resultText.value = ''

    await generateAIWriting({
      prompt,
      systemPrompt,
      temperature: 0.8
    }, (content) => {
      resultText.value += content
    })

    ElMessage.success('优化完成')
  } catch (error) {
    resultText.value = resultText.value
    ElMessage.error('操作失败，请重试')
  } finally {
    isGenerating.value = false
  }
}

const copyResult = async () => {
  if (!resultText.value) return
  try {
    await navigator.clipboard.writeText(resultText.value)
    ElMessage.success('已复制 Markdown 源码')
  } catch (err) {
    ElMessage.error('复制失败')
  }
}

const copyPreviewHtml = async () => {
  if (!resultText.value) return
  try {
    const previewElement = document.querySelector('.vuepress-markdown-body')
    if (previewElement) {
      const htmlContent = previewElement.innerHTML
      const blob = new Blob([htmlContent], { type: 'text/html' })
      const textBlob = new Blob([resultText.value], { type: 'text/plain' })
      const data = [new ClipboardItem({
        'text/html': blob,
        'text/plain': textBlob
      })]
      await navigator.clipboard.write(data)
      ElMessage.success('已复制预览样式内容')
    }
  } catch (err) {
    ElMessage.error('复制预览内容失败')
  }
}

const clearResult = () => {
  resultText.value = ''
  isGenerating.value = false
}

const save = (text: string, html: string) => {
  console.log('save', text, html)
}
</script>

