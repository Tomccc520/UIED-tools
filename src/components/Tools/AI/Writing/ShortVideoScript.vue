<script setup lang="ts">
import { ref, reactive } from 'vue'
import { useRoute } from 'vue-router'
import { ElMessage } from 'element-plus'
import ToolsRecommend from '@/components/Common/ToolsRecommend.vue'
import WritingGuide from './WritingGuide.vue'
import { generateAIWriting } from '@/services/ai'

const route = useRoute()
const mode = ref<'editable' | 'preview' | 'edit'>('editable')
const form = reactive({
  theme: '',
  characters: '',
  duration: 'medium',
  style: 'funny'
})

const isGenerating = ref(false)
const resultText = ref('')

/**
 * 生成内容
 */
const generateContent = async () => {
  if (!form.theme) {
    ElMessage.warning('请输入剧情梗概或主题')
    return
  }

  try {
    isGenerating.value = true
    resultText.value = ''

    // 构建提示词
    const prompt = `请帮我创作一个短视频分镜头脚本。
剧情梗概/主题：${form.theme}
${form.characters ? `角色设定：${form.characters}` : ''}
预计时长：${getDurationLabel(form.duration)}
风格类型：${getStyleLabel(form.style)}

要求：
1. 采用标准的分镜头脚本格式（镜号、画面描述、台词/字幕、运镜/景别、音效/BGM、时长）。
2. 剧情要紧凑，有起承转合，最好有反转或爆点。
3. 画面描述要具体，具有画面感。
4. 台词要口语化，符合人物性格。
5. 开头前3秒要有黄金前三秒的吸引力设计。
6. 请使用Markdown表格格式展示脚本内容。
7. 标题请使用 Markdown 三级标题格式（### 标题），严禁在标题行使用 ** 加粗符号`

    await generateAIWriting({
      prompt,
      systemPrompt: '你是一个专业的短视频编剧和导演，擅长创作高质量的分镜头脚本。',
      temperature: 0.8
    }, (content) => {
      resultText.value += content
    })

    ElMessage.success('生成完成')
  } catch (error) {
    console.error('生成失败:', error)
    ElMessage.error('生成失败，请稍后重试')
  } finally {
    isGenerating.value = false
  }
}

/**
 * AI辅助编辑
 */
const handleAiAssist = async (type: string) => {
  if (!resultText.value) return

  isGenerating.value = true
  const originalText = resultText.value
  let prompt = ''

  switch (type) {
    case 'add_camera':
      prompt = `请对上述脚本中的"运镜"和"画面"部分进行专业细化，增加更具体的镜头语言（如推拉摇移、特写、中景等），保持其他内容不变：\n\n${originalText}`
      break
    case 'add_music':
      prompt = `请为上述脚本推荐更合适的背景音乐（BGM）和音效卡点，增强视频的节奏感和氛围，保持其他内容不变：\n\n${originalText}`
      break
  }

  try {
    resultText.value = ''
    await generateAIWriting({
      prompt,
      systemPrompt: '你是一个专业的短视频导演。'
    }, (chunk) => {
      resultText.value += chunk
    })
  } catch (error) {
    ElMessage.error('AI助手处理失败，请重试')
    resultText.value = originalText
  } finally {
    isGenerating.value = false
  }
}

const getDurationLabel = (val: string) => {
  const map: Record<string, string> = {
    short: '15-30秒',
    medium: '30-60秒',
    long: '1-3分钟'
  }
  return map[val] || '30-60秒'
}

const getStyleLabel = (val: string) => {
  const map: Record<string, string> = {
    funny: '搞笑反转',
    healing: '情感治愈',
    career: '职场干货',
    suspense: '悬疑推理',
    vlog: '生活Vlog'
  }
  return map[val] || '搞笑反转'
}

/**
 * 复制生成结果 (Markdown)
 */
const copyResult = async () => {
  if (!resultText.value) return
  try {
    await navigator.clipboard.writeText(resultText.value)
    ElMessage.success('已复制 Markdown 源码')
  } catch (err) {
    console.error('复制失败', err)
    ElMessage.error('复制失败')
  }
}

/**
 * 复制预览样式 (HTML)
 */
const copyPreviewHtml = async () => {
  if (!resultText.value) return
  try {
    const previewElement = document.querySelector('.vuepress-markdown-body')
    if (previewElement) {
      const htmlContent = previewElement.innerHTML
      const blob = new Blob([htmlContent], { type: 'text/html' })
      const textBlob = new Blob([resultText.value], { type: 'text/plain' })
      const data = [new ClipboardItem({
        'text/html': blob,
        'text/plain': textBlob
      })]
      await navigator.clipboard.write(data)
      ElMessage.success('已复制预览样式内容')
    } else {
      ElMessage.warning('未找到预览内容，请切换到预览模式')
    }
  } catch (err) {
    console.error('复制失败', err)
    ElMessage.error('复制预览内容失败')
  }
}

/**
 * 清空生成结果
 */
const clearResult = () => {
  resultText.value = ''
  isGenerating.value = false
}

/**
 * 保存编辑器内容
 */
const save = (text: string, html: string) => {
  console.log('save', text, html)
}
</script>

