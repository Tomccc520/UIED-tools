<script setup lang="ts">
import { ref, reactive } from 'vue'
import { useRoute } from 'vue-router'
import { ElMessage } from 'element-plus'
import ToolsRecommend from '@/components/Common/ToolsRecommend.vue'
import WritingGuide from './WritingGuide.vue'
import { generateAIWriting } from '@/services/ai'

const route = useRoute()
const mode = ref<'editable' | 'preview' | 'edit'>('editable')
const form = reactive({
  event: '',
  reason: '',
  action: '',
  length: 800
})

const isGenerating = ref(false)
const resultText = ref('')

/**
 * 生成内容
 */
const generateContent = async () => {
  if (!form.event) {
    ElMessage.warning('请输入错误事项')
    return
  }

  try {
    isGenerating.value = true
    resultText.value = ''

    // 构建提示词
    const prompt = `请帮我写一份检讨书，字数约${form.length}字。
错误事项：${form.event}
${form.reason ? `错误原因分析：${form.reason}` : ''}
${form.action ? `改正措施计划：${form.action}` : ''}

要求：
1. 态度要极其诚恳，认识到自己的错误。
2. 对错误原因进行深刻的剖析，不找借口。
3. 提出具体、可行的改正措施和承诺。
4. 语言要庄重、严肃，符合检讨书的规范格式。
5. 包含适当的开头（尊敬的领导/老师等）和结尾（检讨人、日期等占位符）。
6. 请使用Markdown格式。
7. 标题请使用 Markdown 三级标题格式（### 标题），严禁在标题行使用 ** 加粗符号`

    await generateAIWriting({
      prompt,
      systemPrompt: '你是一个善于反思和表达的写作助手，能够帮助用户撰写态度诚恳、逻辑清晰的检讨书。',
      temperature: 0.7
    }, (content) => {
      resultText.value += content
    })

    ElMessage.success('生成完成')
  } catch (error) {
    console.error('生成失败:', error)
    ElMessage.error('生成失败，请稍后重试')
  } finally {
    isGenerating.value = false
  }
}

/**
 * AI辅助编辑
 */
const handleAiAssist = async (type: string) => {
  if (!resultText.value) return

  isGenerating.value = true
  const originalText = resultText.value
  let prompt = ''

  switch (type) {
    case 'more_sincere':
      prompt = `请将以下检讨书修改得更加诚恳，语气更谦卑，表达出更强烈的悔过之意：\n\n${originalText}`
      break
    case 'more_profound':
      prompt = `请将以下检讨书修改得更加深刻，从思想根源上剖析错误，提升思想高度：\n\n${originalText}`
      break
  }

  try {
    resultText.value = ''
    await generateAIWriting({
      prompt,
      systemPrompt: '你是一个专业的写作助手。'
    }, (chunk) => {
      resultText.value += chunk
    })
  } catch (error) {
    ElMessage.error('AI助手处理失败，请重试')
    resultText.value = originalText
  } finally {
    isGenerating.value = false
  }
}

/**
 * 复制生成结果 (Markdown)
 */
const copyResult = async () => {
  if (!resultText.value) return
  try {
    await navigator.clipboard.writeText(resultText.value)
    ElMessage.success('已复制 Markdown 源码')
  } catch (err) {
    console.error('复制失败', err)
    ElMessage.error('复制失败')
  }
}

/**
 * 复制预览样式 (HTML)
 */
const copyPreviewHtml = async () => {
  if (!resultText.value) return
  try {
    const previewElement = document.querySelector('.vuepress-markdown-body')
    if (previewElement) {
      const htmlContent = previewElement.innerHTML
      const blob = new Blob([htmlContent], { type: 'text/html' })
      const textBlob = new Blob([resultText.value], { type: 'text/plain' })
      const data = [new ClipboardItem({
        'text/html': blob,
        'text/plain': textBlob
      })]
      await navigator.clipboard.write(data)
      ElMessage.success('已复制预览样式内容')
    } else {
      ElMessage.warning('未找到预览内容，请切换到预览模式')
    }
  } catch (err) {
    console.error('复制失败', err)
    ElMessage.error('复制预览内容失败')
  }
}

/**
 * 清空生成结果
 */
const clearResult = () => {
  resultText.value = ''
  isGenerating.value = false
}

/**
 * 保存编辑器内容
 */
const save = (text: string, html: string) => {
  console.log('save', text, html)
}
</script>

