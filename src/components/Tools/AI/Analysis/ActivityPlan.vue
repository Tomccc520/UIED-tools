<script setup lang="ts">
import { ref, reactive } from 'vue'
import { useRoute } from 'vue-router'
import { ElMessage } from 'element-plus'
import ToolsRecommend from '@/components/Common/ToolsRecommend.vue'
import { generateAIWriting } from '@/services/ai'

const route = useRoute()
const mode = ref<'editable' | 'preview' | 'edit'>('editable')
const form = reactive({
  theme: '',
  target_audience: '',
  time_location: '',
  requirements: ''
})

const isGenerating = ref(false)
const resultText = ref('')

const generateContent = async () => {
  if (!form.theme) {
    ElMessage.warning('请输入活动主题')
    return
  }

  try {
    isGenerating.value = true
    resultText.value = ''

    const prompt = `请帮我策划一份详细的活动方案。
活动主题：${form.theme}
${form.target_audience ? `目标受众：${form.target_audience}` : ''}
${form.time_location ? `活动时间/地点：${form.time_location}` : ''}
${form.requirements ? `关键要求：${form.requirements}` : ''}

要求：
1. 方案完整，包含活动背景、目标、主题、流程、预算估算、人员分工和应急预案等。
2. 创意新颖，互动性强，具有可执行性。
3. 标题请使用 Markdown 三级标题格式（### 标题），严禁在标题行使用 ** 加粗符号。`

    await generateAIWriting({
      prompt,
      systemPrompt: '你是一个资深的活动策划师，擅长设计富有创意和执行力的活动方案。',
      temperature: 0.8
    }, (content) => {
      resultText.value += content
    })

    ElMessage.success('生成完成')
  } catch (error) {
    ElMessage.error('生成失败，请稍后重试')
  } finally {
    isGenerating.value = false
  }
}

const handleAiAssist = async (type: string) => {
  if (!resultText.value) return

  isGenerating.value = true
  const originalText = resultText.value
  let prompt = ''

  switch (type) {
    case 'creative':
      prompt = `请为以下活动方案增加更多的创意亮点和互动环节，使其更具吸引力：\n\n${originalText}`
      break
    case 'detail':
      prompt = `请细化以下活动方案的执行流程和细节，确保活动顺利进行：\n\n${originalText}`
      break
  }

  try {
    resultText.value = ''
    await generateAIWriting({
      prompt,
      systemPrompt: '你是一个资深的活动策划师，擅长设计富有创意和执行力的活动方案。'
    }, (chunk) => {
      resultText.value += chunk
    })
  } catch (error) {
    ElMessage.error('AI助手处理失败，请重试')
    resultText.value = originalText
  } finally {
    isGenerating.value = false
  }
}

const copyResult = async () => {
  if (!resultText.value) return
  try {
    await navigator.clipboard.writeText(resultText.value)
    ElMessage.success('已复制 Markdown 源码')
  } catch (err) {
    ElMessage.error('复制失败')
  }
}

const copyPreviewHtml = async () => {
  if (!resultText.value) return
  try {
    const previewElement = document.querySelector('.vuepress-markdown-body')
    if (previewElement) {
      const htmlContent = previewElement.innerHTML
      const blob = new Blob([htmlContent], { type: 'text/html' })
      const textBlob = new Blob([resultText.value], { type: 'text/plain' })
      const data = [new ClipboardItem({
        'text/html': blob,
        'text/plain': textBlob
      })]
      await navigator.clipboard.write(data)
      ElMessage.success('已复制预览样式内容')
    }
  } catch (err) {
    ElMessage.error('复制预览内容失败')
  }
}

const clearResult = () => {
  resultText.value = ''
  isGenerating.value = false
}

const save = (text: string, html: string) => {
  console.log('save', text, html)
}
</script>

