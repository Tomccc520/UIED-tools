          }
        }
      }
    }

    // ä¿å­˜æœ€ç»ˆçš„æ¨ç†å†…å®¹
    if (reasoningText) {
      messages.value[currentIndex].reasoning_content = reasoningText
    }

  } catch (error: any) {
